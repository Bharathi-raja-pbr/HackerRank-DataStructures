def arrayManipulation(n, queries):
    # Write your code here
    arr=[0]*(n+1)
    #print(arr)
    
    for i,j,k in queries:
        arr[i-1]+=k
        arr[j]-=k
        #print(arr)
    
    maxi=temp=0
    for i in arr:
        temp+=i
        if temp>maxi:
            maxi=temp
    return maxi

